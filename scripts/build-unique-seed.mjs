async function verifyId(id) {
  try {
    const url = `https://images.pexels.com/photos/${id}/pexels-photo-${id}.jpeg?auto=compress&cs=tinysrgb&w=10&h=10&fit=crop`;
    const resp = await fetch(url, { method: 'HEAD', signal: AbortSignal.timeout(5000) });
    return resp.ok;
  } catch { return false; }
}

async function main() {
  // Step 1: Find all duplicate IDs
  const idToItems = {};
  for (const [name, id] of Object.entries(MAPPING)) {
    if (!idToItems[id]) idToItems[id] = [];
    idToItems[id].push(name);
  }

  const duplicates = {};
  for (const [id, items] of Object.entries(idToItems)) {
    if (items.length > 1) {
      duplicates[id] = items;
    }
  }

  console.log(`Total entries: ${Object.keys(MAPPING).length}`);
  console.log(`Unique IDs: ${Object.keys(idToItems).length}`);
  console.log(`Duplicate IDs: ${Object.keys(duplicates).length}`);
  console.log('\nDuplicate groups:');
  for (const [id, items] of Object.entries(duplicates)) {
    console.log(`  ${id}: ${items.join(', ')}`);
  }

  // Step 2: Verify a sample of IDs
  console.log('\nVerifying sample IDs...');
  const sampleIds = Object.values(MAPPING).filter((v,i,a) => a.indexOf(v) === i).slice(0, 30);
  const results = await Promise.all(sampleIds.map(async id => {
    const ok = await verifyId(id);
    return { id, ok };
  }));

  const valid = results.filter(r => r.ok).length;
  const invalid = results.filter(r => !r.ok);
  console.log(`Valid: ${valid}/${results.length}`);
  if (invalid.length) {
    console.log('Invalid IDs:', invalid.map(r => r.id).join(', '));
  }
}

